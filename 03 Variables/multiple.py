# This code demonstrates how to assign multiple variables in Python.
x, y, z = "Orange", "Banana", "Cherry"
print(x)
print(y)
print(z)

# Assigning the same value to multiple variables
a = b = c = "One"
print(a)
print(b)
print(c)

# Unpacking a collection or Destructuring assignment
fruits = ["Two", "Three", "Four"]
x, y, z = fruits
print(x)
print(y)
print(z)