x = str(3)   # x will be '3'
y = int(3)   # y will be 3
z = float(3) # z will be 3.0
print(x) # Outputs: 3
print(y) # Outputs: 3
print(z) # Outputs: 3.0

print(type(x)) # Outputs: <class 'str'>
print(type(y)) # Outputs: <class 'int'>
print(type(z)) # Outputs: <class 'float'>

