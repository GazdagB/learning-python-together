
# Arithmetic Operators
# +, -, *, /, //, %, **

# Addition
print(5 + 10) 

# Subtraction
print(10 - 5)

# Multiplication
print(5 * 10)

# Division
print(10 / 5) # Returns a float

# Floor Division
print(10 // 3) # Returns an integer

# Modulus
print(10 % 3) # Returns the remainder

# Exponentiation
print(2 ** 3) # 2 raised to the power of 3
