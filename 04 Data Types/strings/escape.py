txt = "We are the so-called \"Vikings\" from the north."
print(txt)

txt = 'It\'s alright' # \' is an escape character fpr single quote
print(txt)

txt = "This will insert one \\ (backslash)." # \ is an escape character for backslash
print(txt)

txt = "Hello\nWorld!" # \n is a newline character
print(txt)

txt = "Hello\tWorld!" # \t is a tab character
print(txt)

txt = "Hello\bWorld!" # \b is a backspace character
print(txt)

txt = "Hello\rWorld!" # \r is a carriage return character